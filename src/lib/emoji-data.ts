/**
 * Search emoji by keyword. Returns matching emoji characters.
 */
export function searchEmoji(query: string): string[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];

  const results = new Set<string>();

  // Check keyword mappings
  for (const [keyword, emojis] of Object.entries(EMOJI_KEYWORDS)) {
    if (keyword.includes(q)) {
      for (const e of emojis) results.add(e);
    }
  }

  // Also check category names
  for (const category of EMOJI_CATEGORIES) {
    if (category.name.toLowerCase().includes(q)) {
      for (const e of category.emojis.slice(0, 20)) results.add(e);
    }
  }

  return Array.from(results);
}
